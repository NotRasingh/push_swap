#include "libft.h"

int	ft_atoi(const char *str)
{
	int			i;
	long int	n;
	int			m;

	i = 0;
	n = 0;
	m = 1;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\n'
			|| str[i] == '\v' || str[i] == '\f' || str[i] == '\r')
		i++;
	if ((str[0] == '+' && str[1] == '-') || (str[0] == '-' && str[1] == '+'))
		return (0);
	if (str[i] == '+')
		i++;
	if (str[i] == '-')
	{
		m = -1;
		i++;
	}
	while (str[i] <= 57 && str[i] >= 48)
	{
		n = n * 10 + (str[i] - 48);
		i++;
	}
	return (n * m);
}
